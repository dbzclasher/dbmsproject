    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String a = t2.getText();
        
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            try
            {
                Class.forName("java.sql.DriverManager");
                Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/project?useSSL=false","root","verygood(12)");
                Statement stmt = (Statement) con.createStatement();
                String query="select * from employee;";
                ResultSet rs=stmt.executeQuery(query);
                while(rs.next())
                {
                    String eid=rs.getString("eid");
                    String zid=rs.getString("zid");
                    String e_name=rs.getString("e_name");
                    String phone_no=rs.getString("phone_no");
                    String salary=rs.getString("salary");
                    String password=rs.getString("password");
                    
                    model.addRow(new Object[] {eid,zid,e_name,phone_no,salary,password});
                    
                }
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }//GEN-LAST:event_jButton4ActionPerformed

