    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String a = t2.getText();
        
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            try
            {
                Class.forName("java.sql.DriverManager");
                Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/project?useSSL=false","root","verygood(12)");
                Statement stmt = (Statement) con.createStatement();
                String query="select * from animals;";
                ResultSet rs=stmt.executeQuery(query);
                while(rs.next())
                {
                    String aid=rs.getString("aid");
                    String asid=rs.getString("asid");
                    String eid=rs.getString("eid");
                    String gender=rs.getString("gender");
                    String cage_number=rs.getString("cage_number");
                    String last_fed_time=rs.getString("last_feed_time");
                    String weight_height=rs.getString("weight_height");
                    model.addRow(new Object[] {aid,asid,eid,gender,cage_number,last_fed_time,weight_height});
                    
                }
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        
    }//GEN-LAST:event_jButton4ActionPerformed

